import java.util.*;

public class HashMap_intro {
    public static void main(String[] args) {
        //create a hashmap
        HashMap<String,Integer> hm = new HashMap<>();

        //insert the element - O(1)
        hm.put("India", 100);
        hm.put("US", 50);
        hm.put("China", 150);

        System.out.println(hm);

        //get - O(1)
        
    }
}
